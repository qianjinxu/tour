package main

import (
	"fmt"
	"math"
)

func pow(x, n, lim float64) float64 {
	// 在 if statement 中声明的 short variable 也可以在任何 else block 内使用
	if v := math.Pow(x, n); v < lim {
		return v
	} else {
		fmt.Printf("%g >= %g\n", v, lim)
	}
	return lim
}

func main() {
	fmt.Println(
		pow(3, 2, 10),
		pow(3, 3, 20),
	)
}
