package main

import (
	"fmt"
	"math"
)

func sqrt(x float64) string {
	// if statement 表达式使用大括号而不是小括号
	if x < 0 {
		return sqrt(-x) + "i"
	}
	return fmt.Sprint(math.Sqrt(x))
}

func main() {
	fmt.Println(sqrt(2), sqrt(-4))
}
